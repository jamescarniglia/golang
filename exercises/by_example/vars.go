package main

import "fmt"

func main() {
	// declare variable
	var a = "JAMES"
	fmt.Println(a)

	// declare multiple variable
	var b, c = "JAMES", "JOHN"
	fmt.Println(b, c)

	// declare int
	var d int = 10
	fmt.Println(d)

	// declare and assign
	f := "WHAT?"
	fmt.Println(f)

	// declare bool expect true
	var q = true
	fmt.Println(q)

	// declare int expect 0
	var z int
	fmt.Println(z)
}
